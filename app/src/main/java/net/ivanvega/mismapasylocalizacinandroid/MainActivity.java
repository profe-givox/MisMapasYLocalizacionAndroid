public class MainActivity extends AppCompatActivity
        implements OnMapReadyCallback {


    SupportMapFragment mapFragment;
    GoogleMap mapa;
    private FusedLocationProviderClient fusedLocationClient;

    @Override
    protected void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);
        setContentView(R.layout.activity_main);

        mapFragment = (SupportMapFragment) getSupportFragmentManager()
                .findFragmentById(R.id.map);


        mapFragment.getMapAsync(this);


    }

    @Override
    public void onMapReady(GoogleMap googleMap) {
        mapa = googleMap;
        mapa.addMarker(new MarkerOptions()
                .position(new LatLng(0, 0))
                .title("Marker"));


        LatLng sydney = new LatLng(-33.852, 151.211);
        googleMap.addMarker(new MarkerOptions()
                .position(sydney)
                .title("Marker in Sydney"));

        CameraUpdate cameraUpdate = CameraUpdateFactory.newLatLng(sydney);

        mapa.animateCamera(cameraUpdate);


        CameraPosition cameraPosition = CameraPosition.builder()
                .target(new LatLng(20.139295, -101.150777))
                .zoom(18)
                .tilt(67)
                .bearing(90).build();

        //cameraUpdate = CameraUpdateFactory.newCameraPosition();

        mapa.animateCamera(CameraUpdateFactory.newCameraPosition(cameraPosition));


        //mapa.setMapType(GoogleMap.MAP_TYPE_HYBRID);

        // Add polylines to the map.
        // Polylines are useful to show a route or some other connection between points.
        Polyline polyline1 = googleMap.addPolyline(new PolylineOptions()
                .clickable(true)
                .add(
                        new LatLng(-35.016, 143.321),
                        new LatLng(-34.747, 145.592),
                        new LatLng(-34.364, 147.891),
                        new LatLng(-33.501, 150.217),
                        new LatLng(-32.306, 149.248),
                        new LatLng(-32.491, 147.309)));

        // Position the map's camera near Alice Springs in the center of Australia,
        // and set the zoom factor so most of Australia shows on the screen.
        googleMap.moveCamera(CameraUpdateFactory.newLatLngZoom(new LatLng(-23.684, 133.903), 4));


        clienteFusedLocation();

    }

    private void clienteFusedLocation() {
        fusedLocationClient = LocationServices.getFusedLocationProviderClient(this);
        if (ActivityCompat.checkSelfPermission(this, Manifest.permission.ACCESS_FINE_LOCATION) != PackageManager.PERMISSION_GRANTED && ActivityCompat.checkSelfPermission(this, Manifest.permission.ACCESS_COARSE_LOCATION) != PackageManager.PERMISSION_GRANTED) {
            // TODO: Consider calling
            //    ActivityCompat#requestPermissions
            // here to request the missing permissions, and then overriding
            //   public void onRequestPermissionsResult(int requestCode, String[] permissions,
            //                                          int[] grantResults)
            // to handle the case where the user grants the permission. See the documentation
            // for ActivityCompat#requestPermissions for more details.
            return;
        }
        fusedLocationClient.getLastLocation()
                .addOnSuccessListener(this, new OnSuccessListener<Location>() {
                    @Override
                    public void onSuccess(Location location) {
                        // Got last known location. In some rare situations this can be null.
                        if (location != null) {
                            // Logic to handle location object
                            Log.d("MIXUBICA", "Lat: " + location.getLatitude() +
                                    " , lon" + location.getLongitude());
                            Toast.makeText(getBaseContext(), "Lat: " + location.getLatitude() +
                                    " , lon" + location.getLongitude(),Toast.LENGTH_LONG )
                                    .show();
                        }
                    }
                });
    }


    @Override
    public void onPointerCaptureChanged(boolean hasCapture) {

    }
}